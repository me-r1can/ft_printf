#include "libft.h"

int		ft_strcmp(char *str1, char *str2)
{
	if (*str1 == *str2)
	{
		if (str1 == '\0' && str2 == '\0')
			return (0);
		return (ft_strcmp((str1 + 1), (str2 + 1)));
	}
	else
		return (*str1 - *str2);
}
